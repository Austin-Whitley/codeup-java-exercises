public class ControlFlowExercises {
    public static void main(String[] args) {
        int i = 5;
        while(i <= 15){
            System.out.println(i);
            i++;
        }

        long x = 2;
        do{
            System.out.println(x);
            x = x * x;
        }while(x <= 1000000);



    }
}
